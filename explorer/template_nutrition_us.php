<h1>U.S. Nutrition Facts</h1>

POD database now contains nutrition facts for products commercialized in the U.S.<br/>
<b>U.S. Food & Drug Administration</b> provides Food labeling requirements to manufacturers and most of them publish these information on their website. 
<a href="http://www.fda.gov/Food/GuidanceRegulation/GuidanceDocumentsRegulatoryInformation/LabelingNutrition/ucm2006828.htm" target="_blank" rel="nofollow">Access to the FDA website for more information</a><br/>
<br/>
Standard Food labels exist in other countries (Australia, Hong Kong, UK,...) and nutrition information of these countries will be added soon. Europe will use a standard label end 2014 only (<a href="http://eur-lex.europa.eu/LexUriServ/LexUriServ.do?uri=OJ:L:2011:304:0018:0063:EN:PDF" target="_blank" rel="nofollow"> European law nÂ° 1169/2011 - 25 Oct. 2011</a>).
<br/><br/>
POD only uses <b>Trusted Source of Data</b> provided by manfacturers. The source and the date of synchronization are always indicated.
